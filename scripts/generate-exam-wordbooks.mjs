import { promises as fs } from "node:fs";
import os from "node:os";
import path from "node:path";

const ECDICT_REVISION = "bc015ed2e24a7abef49fc6dbbb7fe32c1dadaf8b";
const ECDICT_URL = `https://raw.githubusercontent.com/skywind3000/ECDICT/${ECDICT_REVISION}/ecdict.csv`;
const CACHE_PATH = path.join(os.tmpdir(), `ecdict-${ECDICT_REVISION}.csv`);
const OUTPUT_PATH = path.resolve("app/wordbooks-data/exam-wordbooks.ts");
const MAX_WORD_LENGTH = 24;

async function loadSource() {
  try {
    const cached = await fs.readFile(CACHE_PATH, "utf8");
    if (cached.startsWith("word,phonetic,")) return cached;
  } catch {
    // Download the pinned source below.
  }

  const response = await fetch(ECDICT_URL);
  if (!response.ok) throw new Error(`ECDICT download failed with HTTP ${response.status}.`);
  const source = await response.text();
  await fs.writeFile(CACHE_PATH, source, "utf8");
  return source;
}

function visitCsv(source, visitor) {
  let row = [];
  let field = "";
  let quoted = false;

  for (let index = 0; index < source.length; index += 1) {
    const character = source[index];
    if (quoted) {
      if (character === '"') {
        if (source[index + 1] === '"') {
          field += '"';
          index += 1;
        } else {
          quoted = false;
        }
      } else {
        field += character;
      }
      continue;
    }

    if (character === '"' && field.length === 0) {
      quoted = true;
    } else if (character === ",") {
      row.push(field);
      field = "";
    } else if (character === "\n") {
      row.push(field.endsWith("\r") ? field.slice(0, -1) : field);
      visitor(row);
      row = [];
      field = "";
    } else {
      field += character;
    }
  }

  if (field || row.length) {
    row.push(field);
    visitor(row);
  }
}

function formatArray(name, words) {
  const lines = [];
  for (let index = 0; index < words.length; index += 8) {
    lines.push(`  ${words.slice(index, index + 8).map((word) => JSON.stringify(word)).join(", ")},`);
  }
  return `export const ${name} = [\n${lines.join("\n")}\n] as const;`;
}

const source = await loadSource();
const books = {
  cet4: new Set(),
  cet6: new Set(),
  postgraduate: new Set(),
};
let columns = null;

visitCsv(source, (row) => {
  if (!columns) {
    columns = Object.fromEntries(row.map((name, index) => [name, index]));
    return;
  }
  const word = String(row[columns.word] ?? "").trim().toLowerCase();
  if (!/^[a-z]+$/.test(word) || word.length < 2 || word.length > MAX_WORD_LENGTH) return;
  const tags = new Set(String(row[columns.tag] ?? "").split(/\s+/).filter(Boolean));
  if (tags.has("cet4")) books.cet4.add(word);
  if (tags.has("cet6")) books.cet6.add(word);
  if (tags.has("ky")) books.postgraduate.add(word);
});

const sorted = Object.fromEntries(
  Object.entries(books).map(([id, words]) => [id, [...words].sort((left, right) => left.localeCompare(right))]),
);
for (const [id, words] of Object.entries(sorted)) {
  if (words.length < 500) throw new Error(`${id} only produced ${words.length} playable words.`);
}

const output = [
  "// Generated by scripts/generate-exam-wordbooks.mjs. Do not edit by hand.",
  "// Source: ECDICT (MIT), pinned revision below.",
  `export const ECDICT_SOURCE_REVISION = ${JSON.stringify(ECDICT_REVISION)} as const;`,
  "",
  formatArray("ECDICT_CET4_WORDS", sorted.cet4),
  "",
  formatArray("ECDICT_CET6_WORDS", sorted.cet6),
  "",
  formatArray("ECDICT_POSTGRADUATE_WORDS", sorted.postgraduate),
  "",
].join("\n");

await fs.mkdir(path.dirname(OUTPUT_PATH), { recursive: true });
await fs.writeFile(OUTPUT_PATH, output, "utf8");

console.log(JSON.stringify({
  sourceRevision: ECDICT_REVISION,
  output: path.relative(process.cwd(), OUTPUT_PATH),
  counts: Object.fromEntries(Object.entries(sorted).map(([id, words]) => [id, words.length])),
  longest: Object.fromEntries(Object.entries(sorted).map(([id, words]) => [
    id,
    [...words].sort((left, right) => right.length - left.length || left.localeCompare(right)).slice(0, 10),
  ])),
}, null, 2));
